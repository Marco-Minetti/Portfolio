    <h2 class="sub-title">Italiano</h2>

    <p> 
        La lettura del libro "<a href="https://www.centropace-friedenszentrum.com/it/evento/senza-sbarre-storia-di-un-carcere-aperto" target="_blank" rel="noreferrer">Senza sbarre</a>" è stata seguita da un incontro con le scrittrici di Cosima Buccoliero e Serena Uccello, coautrice. Il loro approccio alla situazione carceraria in Italia è fondamentalmente diverso dal consolidato pensiero per il quale il carcere è un luogo dove espiare le proprie colpe.
        Per Cosima Buccoliero, il carcere è accoglienza e umanità, un luogo brulicante di vitalità. Ci sono i carcerati, il personale di sorveglianza e medico, i tanti volontari e le loro famiglie.
        Per lei la pena detentiva deve mirare a un reinserimento e non ridursi alla sola punizione. La detenzione non deve perdere la sua funzione rieducativa, altrimenti diventa solo dolore, tormento e sofferenza.
        <br>
        <br>
        Il libro "<a href="https://www.ibs.it/altra-verita-diario-di-diversa-libro-alda-merini/e/9788817065351" target="_blank" rel="noreferrer">L'altra verità</a>", scritto da Alda Merini, racconta, senza filtri, gli orrori che ogni giorno si perpetuavano dietro le porte del manicomio. Persone ridotte ad oggetti, denudate e rivestite di camici anonimi, private delle libertà più elementari, legate mani e piedi e costrette a vivere in condizioni inumane in mezzo ai propri escrementi.
        Questa era la nuda realtà dei manicomi italiani prima della legge Basaglia.

    </p>
